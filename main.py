def tick():
    #Eye constants
    global bECS
    global bECST
    global bF
    global lECS
    global lECST
    global lF
    global rECS
    global rECST
    global rF
    global timerColour
    global sR
    global lRC
    global lCC
    global letters
    global cS
    global wT
    global ui
    global row5
    global row6
    #global vidOpen
    ret, img = vid.read()
    #cv2.resize(img, (100, 100))
    gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)
    faces = face_cascade.detectMultiScale(gray, 1.3, 5)
    if len(faces)==1:
        #Draw Face
        for (x,y,w,h) in faces:
            cv2.rectangle(img,(x,y),(x+w,y+h),(255,0,0),2)
            roi_gray = gray[y:y+h, x:x+w]
            roi_grayLeft = gray[y:y+h, x:x+w//2]
            roi_grayRight = gray[y:y+h, x+w//2:x+w]
            roi_color = img[y:y+h, x:x+w]
            leftEye = eye_cascade.detectMultiScale(roi_grayLeft)
            rightEye = eye_cascade.detectMultiScale(roi_grayRight)

            if len(leftEye)==0 and len(rightEye)==0:
                if bECS==False:
                    timerColour.stop()
                    bECST = time.time()
                    bECS = True
            elif time.time()-bECST>.75 and bF==0:
                bF=1
                if sR==False:
                    sR=True
                else:
                    sR=False
                    if lRC<5:
                        cS = cS + letters[lRC][lCC]
                        if len(cS)==1:
                            cS=cS.upper()
                    elif lRC==5:
                        row5[lCC]()
                    elif lRC==6:
                        row6[lCC]()
                    # elif lRC==7:
                    #     row7[lCC]()
                    if ui.go:
                        ui.textBrowser_3.setText(cS)
                        lRC=-1
                        lCC=-1

            elif len(leftEye)==1 and len(rightEye)==0:
                if rECS==False:
                    timerColour.stop()
                    rECST=time.time()
                    rECS=True
                elif time.time()-rECST>1.5 and rF==0:
                    rF=1
            ##Right Wink
            elif len(leftEye)==0 and len(rightEye)==1:
                if lECS==False:
                    timerColour.stop()
                    lECST=time.time()
                    lECS=True
                elif time.time()-lECST>1.5 and lF==0:
                    lF=1
            ##Eyes Open
            elif len(leftEye)==1 and len(rightEye)==1:
                bF=0
                bECS=False
                bECST=time.time()
                rF=0
                rECS=False
                lECST=time.time()
                lF=0
                lECS=False
                rECST=time.time()
                if timerColour.isActive()==False:
                    timerColour.start(1000)

            for (ex,ey,ew,eh) in rightEye:
                cv2.rectangle(roi_color,(ex+w//2,ey),(ex+ew+w//2,ey+eh),(255,255,0),2)
            for (ex,ey,ew,eh) in leftEye:
                cv2.rectangle(roi_color,(ex,ey),(ex+ew,ey+eh),(0,255,0),2)
    cv2.imshow('Blink-2-Voice',img)
    # k = cv2.waitKey(30) & 0xff
    # if k == 27:
    #
    #     cap.release()
    #     cv2.destroyAllWindows()
    #     server.quit()
    #     return 0
    if cv2.waitKey(30) & 0xFF == ord('b'):
        vid.release()
        cv2.waitKey(0)
        cv2.destroyAllWindows()
        return 0


